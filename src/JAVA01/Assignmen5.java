package JAVA01;

// Java code to illustrate "%="

import java.io.*;

class Assignment5 {
    public static void main(String[] args)
    {

        // Declaring variables
        int num1 = 5, num2 = 3;

        System.out.println("num1 = " + num1);
        System.out.println("num2 = " + num2);

        // Modeling & Assigning values
        num1 %= num2;

        // Displaying the assigned values
        System.out.println("num1 = " + num1);
    }
}

