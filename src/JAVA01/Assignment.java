package JAVA01;

// Java code to illustrate "=" operator

import java.io.*;

class Assignment {
    public static void main(String[] args)
    {
        // Declaring variables
        int num;
        String name;

        // Assigning values
        num = 10;
        name = "GeeksforGeeks";

        int num1=12;
        String name1="GeekForGeeks";

        // Displaying the assigned values
        System.out.println("num is assigned: " + num);
        System.out.println("name is assigned: " + name);

        System.out.println("num1 is assigned: " + num1);
        System.out.println("name1 is assigned: " + name1);
    }
}
